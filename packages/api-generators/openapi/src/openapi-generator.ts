/**
 * OpenAPI Generator
 * 
 * Generates OpenAPI 3.1 specifications from model and route definitions
 */

import type { RouteDefinition } from '@web-loom/api-core';
import type { HTTPMethod } from '@web-loom/api-shared';
import type {
  OpenAPISpec,
  OpenAPIGeneratorOptions,
  OpenAPISchema,
  OpenAPIPathItem,
  OpenAPIOperation,
  OpenAPIParameter,
  OpenAPIRequestBody,
  OpenAPIResponse,
  OpenAPIComponents,
  OpenAPIInfo,
  OpenAPIServer,
  OpenAPISecurityScheme,
  OpenAPISecurityRequirement,
  OpenAPITag,
  OpenAPIContact,
  OpenAPILicense,
  OpenAPIExternalDocs,
} from './types';
import { fieldToSchema, generateExampleValue, type FieldDefinition } from './schema-mapper';

/**
 * Model definition for OpenAPI generation
 */
export interface ModelDefinition {
  name: string;
  tableName?: string;
  fields: FieldDefinition[];
  relationships?: Array<{
    type: 'hasOne' | 'hasMany' | 'belongsTo' | 'manyToMany';
    model: string;
    foreignKey?: string;
    localKey?: string;
    through?: string;
    as?: string;
  }>;
  options?: {
    timestamps?: boolean;
    softDelete?: boolean;
    optimisticLocking?: boolean;
    crud?: boolean;
    permissions?: Record<string, string[]>;
  };
  metadata?: {
    description?: string;
    tags?: string[];
    deprecated?: boolean;
    version?: string;
    examples?: Record<string, unknown>[];
  };
}

/**
 * OpenAPI Generator Class
 * 
 * Generates OpenAPI 3.1 specifications from Web Loom models and routes
 */
export class OpenAPIGenerator {
  private models: Map<string, ModelDefinition> = new Map();
  private routes: RouteDefinition[] = [];
  private options: {
    title: string;
    version: string;
    description: string;
    servers: OpenAPIServer[];
    contact?: OpenAPIContact;
    license?: OpenAPILicense;
    securitySchemes: Record<string, OpenAPISecurityScheme>;
    security: OpenAPISecurityRequirement[];
    tags: OpenAPITag[];
    externalDocs?: OpenAPIExternalDocs;
    includeExamples: boolean;
    includeDeprecated: boolean;
  };

  constructor(options: OpenAPIGeneratorOptions = {}) {
    this.options = {
      title: options.title || 'Web Loom API',
      version: options.version || '1.0.0',
      description: options.description || 'API generated by Web Loom Framework',
      servers: options.servers || [{ url: 'http://localhost:3000' }],
      securitySchemes: options.securitySchemes || {},
      security: options.security || [],
      tags: options.tags || [],
      includeExamples: options.includeExamples ?? true,
      includeDeprecated: options.includeDeprecated ?? true,
    };

    // Only assign optional properties if they exist
    if (options.contact) {
      this.options.contact = options.contact;
    }

    if (options.license) {
      this.options.license = options.license;
    }

    if (options.externalDocs) {
      this.options.externalDocs = options.externalDocs;
    }
  }

  /**
   * Register a model definition
   */
  registerModel(model: ModelDefinition): void {
    this.models.set(model.name, model);
  }

  /**
   * Register multiple model definitions
   */
  registerModels(models: ModelDefinition[]): void {
    for (const model of models) {
      this.registerModel(model);
    }
  }

  /**
   * Register a route definition
   */
  registerRoute(route: RouteDefinition): void {
    this.routes.push(route);
  }

  /**
   * Register multiple route definitions
   */
  registerRoutes(routes: RouteDefinition[]): void {
    this.routes.push(...routes);
  }

  /**
   * Generate OpenAPI specification
   */
  generate(): OpenAPISpec {
    const info: OpenAPIInfo = {
      title: this.options.title,
      version: this.options.version,
      description: this.options.description,
    };

    if (this.options.contact) {
      info.contact = this.options.contact;
    }

    if (this.options.license) {
      info.license = this.options.license;
    }

    const spec: OpenAPISpec = {
      openapi: '3.1.0',
      info,
      servers: this.options.servers,
      paths: this.generatePaths(),
      components: this.generateComponents(),
    };

    // Add security if configured
    if (this.options.security.length > 0) {
      spec.security = this.options.security;
    }

    // Add tags if configured
    if (this.options.tags.length > 0) {
      spec.tags = this.options.tags;
    }

    // Add external docs if configured
    if (this.options.externalDocs) {
      spec.externalDocs = this.options.externalDocs;
    }

    return spec;
  }

  /**
   * Generate paths from routes
   */
  private generatePaths(): Record<string, OpenAPIPathItem> {
    const paths: Record<string, OpenAPIPathItem> = {};

    for (const route of this.routes) {
      // Skip deprecated routes if not included
      if (route.metadata?.deprecated && !this.options.includeDeprecated) {
        continue;
      }

      // Convert :param to {param} format for OpenAPI
      const path = route.path.replace(/:([a-zA-Z0-9_]+)/g, '{$1}');

      // Initialize path item if not exists
      if (!paths[path]) {
        paths[path] = {};
      }

      // Add operation for this method
      const method = route.method.toLowerCase() as Lowercase<HTTPMethod>;
      paths[path][method] = this.generateOperation(route);
    }

    return paths;
  }

  /**
   * Generate operation from route
   */
  private generateOperation(route: RouteDefinition): OpenAPIOperation {
    const operation: OpenAPIOperation = {
      summary: route.metadata?.description || `${route.method} ${route.path}`,
      operationId: this.generateOperationId(route),
      parameters: this.generateParameters(route),
      responses: this.generateResponses(route),
    };

    // Add optional fields only if they exist
    if (route.metadata?.description) {
      operation.description = route.metadata.description;
    }

    if (route.metadata?.tags && route.metadata.tags.length > 0) {
      operation.tags = route.metadata.tags;
    }

    if (route.metadata?.deprecated) {
      operation.deprecated = true;
    }

    // Add request body for POST, PUT, PATCH
    if (['POST', 'PUT', 'PATCH'].includes(route.method)) {
      const requestBody = this.generateRequestBody(route);
      if (requestBody) {
        operation.requestBody = requestBody;
      }
    }

    // Add security requirements
    if (route.auth?.required) {
      operation.security = this.generateSecurityRequirements(route);
    }

    // Add rate limiting as OpenAPI extension
    if (route.rateLimit) {
      (operation as unknown as Record<string, unknown>)['x-rate-limit'] = {
        limit: route.rateLimit.limit,
        window: route.rateLimit.window,
        windowUnit: 'milliseconds',
      };
    }

    // Add caching configuration as OpenAPI extension
    if (route.cache) {
      (operation as unknown as Record<string, unknown>)['x-cache'] = {
        ttl: route.cache.ttl,
        ttlUnit: 'seconds',
        perUser: route.cache.perUser || false,
      };
    }

    return operation;
  }

  /**
   * Generate operation ID from route
   */
  private generateOperationId(route: RouteDefinition): string {
    const method = route.method.toLowerCase();
    const pathParts = route.path
      .split('/')
      .filter(Boolean)
      .map(part => part.replace(/[^a-zA-Z0-9]/g, ''));
    
    return `${method}${pathParts.map(p => p.charAt(0).toUpperCase() + p.slice(1)).join('')}`;
  }

  /**
   * Generate parameters from route
   */
  private generateParameters(route: RouteDefinition): OpenAPIParameter[] {
    const parameters: OpenAPIParameter[] = [];

    // Extract path parameters
    const pathParams = route.path.match(/:([a-zA-Z0-9_]+)/g);
    if (pathParams) {
      for (const param of pathParams) {
        const paramName = param.substring(1);
        
        // Check if there's a validation schema for this parameter
        let schema: OpenAPISchema = { type: 'string' };
        if (route.validation?.params && typeof route.validation.params === 'object') {
          const paramsSchema = route.validation.params as Record<string, unknown>;
          if (paramName in paramsSchema) {
            schema = this.convertValidationSchemaToOpenAPI(paramsSchema[paramName]);
          }
        }

        parameters.push({
          name: paramName,
          in: 'path',
          required: true,
          schema,
          description: `${paramName} parameter`,
        });
      }
    }

    // Add query parameters from validation schema
    if (route.validation?.query && typeof route.validation.query === 'object') {
      const querySchema = route.validation.query as Record<string, unknown>;
      for (const [name, schema] of Object.entries(querySchema)) {
        parameters.push({
          name,
          in: 'query',
          required: false,
          schema: this.convertValidationSchemaToOpenAPI(schema),
          description: `${name} query parameter`,
        });
      }
    } else if (route.method === 'GET') {
      // Add common pagination parameters for GET requests if no validation schema
      parameters.push(
        {
          name: 'page',
          in: 'query',
          required: false,
          schema: { type: 'integer', minimum: 1, default: 1 },
          description: 'Page number for pagination',
        },
        {
          name: 'limit',
          in: 'query',
          required: false,
          schema: { type: 'integer', minimum: 1, maximum: 100, default: 20 },
          description: 'Number of items per page',
        }
      );
    }

    // Add header parameters from validation schema
    if (route.validation?.headers && typeof route.validation.headers === 'object') {
      const headersSchema = route.validation.headers as Record<string, unknown>;
      for (const [name, schema] of Object.entries(headersSchema)) {
        parameters.push({
          name,
          in: 'header',
          required: false,
          schema: this.convertValidationSchemaToOpenAPI(schema),
          description: `${name} header`,
        });
      }
    }

    return parameters;
  }

  /**
   * Generate request body from route
   */
  private generateRequestBody(route: RouteDefinition): OpenAPIRequestBody | undefined {
    // If route has validation schema for body, use it
    if (route.validation?.body) {
      return {
        description: 'Request body',
        required: true,
        content: {
          'application/json': {
            schema: this.convertValidationSchemaToOpenAPI(route.validation.body),
          },
        },
      };
    }

    // Try to infer schema from route path (e.g., /users -> User model)
    const modelName = this.inferModelFromPath(route.path);
    const model = modelName ? this.models.get(modelName) : undefined;

    if (model) {
      return {
        description: `${model.name} object`,
        required: true,
        content: {
          'application/json': {
            schema: { $ref: `#/components/schemas/${model.name}` },
          },
        },
      };
    }

    // Default generic object schema
    return {
      description: 'Request body',
      required: true,
      content: {
        'application/json': {
          schema: { type: 'object' },
        },
      },
    };
  }

  /**
   * Convert validation schema to OpenAPI schema
   * This is a simplified converter - in production, you'd want to handle Zod schemas properly
   */
  private convertValidationSchemaToOpenAPI(schema: unknown): OpenAPISchema {
    // If it's already an OpenAPI schema, return it
    if (typeof schema === 'object' && schema !== null && 'type' in schema) {
      return schema as OpenAPISchema;
    }

    // Default to object type
    return { type: 'object' };
  }

  /**
   * Generate responses from route
   */
  private generateResponses(route: RouteDefinition): Record<string, OpenAPIResponse> {
    const responses: Record<string, OpenAPIResponse> = {};

    // Use metadata responses if available
    if (route.metadata?.responses && route.metadata.responses.length > 0) {
      for (const response of route.metadata.responses) {
        const responseObj: OpenAPIResponse = {
          description: response.description,
        };

        if (response.schema) {
          responseObj.content = {
            'application/json': {
              schema: response.schema as OpenAPISchema,
            },
          };
        }

        responses[response.status.toString()] = responseObj;
      }
      return responses;
    }

    // Generate default responses based on method
    const modelName = this.inferModelFromPath(route.path);
    const model = modelName ? this.models.get(modelName) : undefined;

    switch (route.method) {
      case 'GET':
        if (route.path.includes(':')) {
          // Single resource
          const getResponse: OpenAPIResponse = {
            description: 'Successful response',
          };
          if (model) {
            getResponse.content = {
              'application/json': {
                schema: { $ref: `#/components/schemas/${model.name}` },
                ...(this.options.includeExamples && {
                  example: this.generateModelExample(model),
                }),
              },
            };
          }
          responses['200'] = getResponse;
        } else {
          // List of resources
          const listResponse: OpenAPIResponse = {
            description: 'Successful response with pagination',
          };
          if (model) {
            listResponse.content = {
              'application/json': {
                schema: {
                  type: 'object',
                  properties: {
                    data: {
                      type: 'array',
                      items: { $ref: `#/components/schemas/${model.name}` },
                    },
                    pagination: {
                      type: 'object',
                      properties: {
                        page: { type: 'integer', description: 'Current page number' },
                        limit: { type: 'integer', description: 'Items per page' },
                        total: { type: 'integer', description: 'Total number of items' },
                        totalPages: { type: 'integer', description: 'Total number of pages' },
                      },
                      required: ['page', 'limit', 'total', 'totalPages'],
                    },
                  },
                  required: ['data', 'pagination'],
                },
                ...(this.options.includeExamples && {
                  example: {
                    data: [this.generateModelExample(model)],
                    pagination: {
                      page: 1,
                      limit: 20,
                      total: 100,
                      totalPages: 5,
                    },
                  },
                }),
              },
            };
          }
          responses['200'] = listResponse;
        }
        responses['404'] = {
          description: 'Resource not found',
          content: {
            'application/json': {
              schema: {
                type: 'object',
                properties: {
                  error: {
                    type: 'object',
                    properties: {
                      code: { type: 'string', example: 'NOT_FOUND' },
                      message: { type: 'string', example: 'Resource not found' },
                      timestamp: { type: 'string', format: 'date-time' },
                      requestId: { type: 'string', format: 'uuid' },
                    },
                  },
                },
              },
            },
          },
        };
        break;

      case 'POST':
        // eslint-disable-next-line no-case-declarations
        const postResponse: OpenAPIResponse = {
          description: 'Resource created successfully',
        };
        if (model) {
          postResponse.content = {
            'application/json': {
              schema: { $ref: `#/components/schemas/${model.name}` },
              ...(this.options.includeExamples && {
                example: this.generateModelExample(model),
              }),
            },
          };
        }
        responses['201'] = postResponse;
        responses['400'] = {
          description: 'Invalid request body',
          content: {
            'application/json': {
              schema: {
                type: 'object',
                properties: {
                  error: {
                    type: 'object',
                    properties: {
                      code: { type: 'string', example: 'VALIDATION_ERROR' },
                      message: { type: 'string', example: 'Validation failed' },
                      details: {
                        type: 'object',
                        properties: {
                          fields: {
                            type: 'array',
                            items: {
                              type: 'object',
                              properties: {
                                path: { type: 'array', items: { type: 'string' } },
                                message: { type: 'string' },
                                code: { type: 'string' },
                              },
                            },
                          },
                        },
                      },
                      timestamp: { type: 'string', format: 'date-time' },
                      requestId: { type: 'string', format: 'uuid' },
                    },
                  },
                },
              },
            },
          },
        };
        break;

      case 'PUT':
      case 'PATCH':
        // eslint-disable-next-line no-case-declarations
        const updateResponse: OpenAPIResponse = {
          description: 'Resource updated successfully',
        };
        if (model) {
          updateResponse.content = {
            'application/json': {
              schema: { $ref: `#/components/schemas/${model.name}` },
              ...(this.options.includeExamples && {
                example: this.generateModelExample(model),
              }),
            },
          };
        }
        responses['200'] = updateResponse;
        responses['400'] = {
          description: 'Invalid request body',
          content: {
            'application/json': {
              schema: {
                type: 'object',
                properties: {
                  error: {
                    type: 'object',
                    properties: {
                      code: { type: 'string', example: 'VALIDATION_ERROR' },
                      message: { type: 'string' },
                      timestamp: { type: 'string', format: 'date-time' },
                      requestId: { type: 'string', format: 'uuid' },
                    },
                  },
                },
              },
            },
          },
        };
        responses['404'] = {
          description: 'Resource not found',
          content: {
            'application/json': {
              schema: {
                type: 'object',
                properties: {
                  error: {
                    type: 'object',
                    properties: {
                      code: { type: 'string', example: 'NOT_FOUND' },
                      message: { type: 'string' },
                      timestamp: { type: 'string', format: 'date-time' },
                      requestId: { type: 'string', format: 'uuid' },
                    },
                  },
                },
              },
            },
          },
        };
        break;

      case 'DELETE':
        responses['200'] = {
          description: 'Resource deleted successfully',
          content: {
            'application/json': {
              schema: {
                type: 'object',
                properties: {
                  message: { type: 'string', example: 'Resource deleted successfully' },
                },
              },
            },
          },
        };
        responses['404'] = {
          description: 'Resource not found',
          content: {
            'application/json': {
              schema: {
                type: 'object',
                properties: {
                  error: {
                    type: 'object',
                    properties: {
                      code: { type: 'string', example: 'NOT_FOUND' },
                      message: { type: 'string' },
                      timestamp: { type: 'string', format: 'date-time' },
                      requestId: { type: 'string', format: 'uuid' },
                    },
                  },
                },
              },
            },
          },
        };
        break;

      default:
        responses['200'] = {
          description: 'Successful response',
        };
    }

    // Add common error responses
    if (route.auth?.required) {
      responses['401'] = {
        description: 'Unauthorized - Authentication required',
        content: {
          'application/json': {
            schema: {
              type: 'object',
              properties: {
                error: {
                  type: 'object',
                  properties: {
                    code: { type: 'string', example: 'UNAUTHORIZED' },
                    message: { type: 'string', example: 'Authentication required' },
                    timestamp: { type: 'string', format: 'date-time' },
                    requestId: { type: 'string', format: 'uuid' },
                  },
                },
              },
            },
          },
        },
      };

      if (route.auth.roles && route.auth.roles.length > 0) {
        responses['403'] = {
          description: 'Forbidden - Insufficient permissions',
          content: {
            'application/json': {
              schema: {
                type: 'object',
                properties: {
                  error: {
                    type: 'object',
                    properties: {
                      code: { type: 'string', example: 'FORBIDDEN' },
                      message: { type: 'string', example: 'Insufficient permissions' },
                      timestamp: { type: 'string', format: 'date-time' },
                      requestId: { type: 'string', format: 'uuid' },
                    },
                  },
                },
              },
            },
          },
        };
      }
    }

    if (route.rateLimit) {
      responses['429'] = {
        description: 'Too Many Requests - Rate limit exceeded',
        content: {
          'application/json': {
            schema: {
              type: 'object',
              properties: {
                error: {
                  type: 'object',
                  properties: {
                    code: { type: 'string', example: 'RATE_LIMIT_EXCEEDED' },
                    message: { type: 'string', example: 'Rate limit exceeded' },
                    details: {
                      type: 'object',
                      properties: {
                        retryAfter: { type: 'integer', description: 'Seconds until retry is allowed' },
                      },
                    },
                    timestamp: { type: 'string', format: 'date-time' },
                    requestId: { type: 'string', format: 'uuid' },
                  },
                },
              },
            },
          },
        },
      };
    }

    responses['500'] = {
      description: 'Internal Server Error',
      content: {
        'application/json': {
          schema: {
            type: 'object',
            properties: {
              error: {
                type: 'object',
                properties: {
                  code: { type: 'string', example: 'INTERNAL_ERROR' },
                  message: { type: 'string', example: 'An unexpected error occurred' },
                  timestamp: { type: 'string', format: 'date-time' },
                  requestId: { type: 'string', format: 'uuid' },
                },
              },
            },
          },
        },
      },
    };

    return responses;
  }

  /**
   * Generate security requirements from route
   */
  private generateSecurityRequirements(route: RouteDefinition): Array<Record<string, string[]>> {
    const requirements: Array<Record<string, string[]>> = [];

    // If route has specific scopes, use them
    if (route.auth?.scopes && route.auth.scopes.length > 0) {
      // Assume first security scheme
      const schemeName = Object.keys(this.options.securitySchemes)[0];
      if (schemeName) {
        requirements.push({ [schemeName]: route.auth.scopes });
      }
    } else if (route.auth?.required) {
      // Use empty scopes for required auth
      const schemeName = Object.keys(this.options.securitySchemes)[0];
      if (schemeName) {
        requirements.push({ [schemeName]: [] });
      }
    }

    return requirements;
  }

  /**
   * Generate components (schemas, security schemes, etc.)
   */
  private generateComponents(): OpenAPIComponents {
    const components: OpenAPIComponents = {
      schemas: this.generateSchemas(),
    };

    // Add security schemes if configured
    if (Object.keys(this.options.securitySchemes).length > 0) {
      components.securitySchemes = this.options.securitySchemes;
    }

    return components;
  }

  /**
   * Generate schemas from models
   */
  private generateSchemas(): Record<string, OpenAPISchema> {
    const schemas: Record<string, OpenAPISchema> = {};

    for (const [name, model] of this.models) {
      schemas[name] = this.modelToSchema(model);
    }

    return schemas;
  }

  /**
   * Convert model definition to OpenAPI schema
   */
  private modelToSchema(model: ModelDefinition): OpenAPISchema {
    const properties: Record<string, OpenAPISchema> = {};
    const required: string[] = [];

    for (const field of model.fields) {
      properties[field.name] = fieldToSchema(field);

      // Add to required array if field is required
      if (field.validation?.required) {
        required.push(field.name);
      }
    }

    const schema: OpenAPISchema = {
      type: 'object',
      title: model.name,
      properties,
    };

    // Add optional fields only if they exist
    if (model.metadata?.description) {
      schema.description = model.metadata.description;
    }

    if (required.length > 0) {
      schema.required = required;
    }

    // Add example if enabled
    if (this.options.includeExamples && model.metadata?.examples && model.metadata.examples.length > 0) {
      schema.example = model.metadata.examples[0];
    } else if (this.options.includeExamples) {
      schema.example = this.generateModelExample(model);
    }

    return schema;
  }

  /**
   * Generate example object for a model
   */
  private generateModelExample(model: ModelDefinition): Record<string, unknown> {
    const example: Record<string, unknown> = {};

    for (const field of model.fields) {
      if (!field.computed) {
        example[field.name] = generateExampleValue(field.type, field.name);
      }
    }

    return example;
  }

  /**
   * Infer model name from route path
   * Examples:
   *   /users -> User
   *   /posts/:id -> Post
   *   /api/v1/products -> Product
   */
  private inferModelFromPath(path: string): string | null {
    const parts = path.split('/').filter(Boolean);
    
    // Find the first part that doesn't start with : and isn't 'api' or version
    for (const part of parts) {
      if (!part.startsWith(':') && !part.match(/^(api|v\d+)$/i)) {
        // Convert plural to singular and capitalize
        const singular = part.endsWith('s') ? part.slice(0, -1) : part;
        return singular.charAt(0).toUpperCase() + singular.slice(1);
      }
    }

    return null;
  }

  /**
   * Export specification as JSON string
   */
  toJSON(pretty = true): string {
    const spec = this.generate();
    return JSON.stringify(spec, null, pretty ? 2 : 0);
  }

  /**
   * Export specification as YAML string
   */
  toYAML(): string {
    // Simple YAML conversion (for production, use a proper YAML library)
    const spec = this.generate();
    return this.objectToYAML(spec);
  }

  /**
   * Simple object to YAML converter
   */
  private objectToYAML(obj: unknown, indent = 0): string {
    const spaces = '  '.repeat(indent);
    
    if (obj === null || obj === undefined) {
      return 'null';
    }
    
    if (typeof obj === 'string') {
      return obj.includes('\n') ? `|\n${spaces}  ${obj.replace(/\n/g, `\n${spaces}  `)}` : obj;
    }
    
    if (typeof obj === 'number' || typeof obj === 'boolean') {
      return String(obj);
    }
    
    if (Array.isArray(obj)) {
      if (obj.length === 0) return '[]';
      return '\n' + obj.map(item => `${spaces}- ${this.objectToYAML(item, indent + 1)}`).join('\n');
    }
    
    if (typeof obj === 'object') {
      const entries = Object.entries(obj);
      if (entries.length === 0) return '{}';
      
      return '\n' + entries
        .map(([key, value]) => {
          const yamlValue = this.objectToYAML(value, indent + 1);
          return `${spaces}${key}:${yamlValue.startsWith('\n') ? yamlValue : ' ' + yamlValue}`;
        })
        .join('\n');
    }
    
    return String(obj);
  }
}
